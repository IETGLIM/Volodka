// ============================================
// REVIEWER AGENT
// ============================================
// Критикует и находит слабые места в предложениях
// предыдущих агентов. Конструктивная критика.

import type { AgentContext, AgentResult } from './types';

const SYSTEM_PROMPT = `Ты — Рецензент для RPG/Visual Novel "ВОЛОДЬКА".
Ты критикуешь и находишь слабые места в предложениях предыдущих агентов.

Игра основана на реальных стихах Владимира Лебедева. Стихи — священны. Никаких изменений текста стихов.

Твоя задача: найти проблемы и предложить исправления.
Сосредоточься на:
- Логических противоречиях в сюжете/механиках
- Балансе сложности (не слишком легко, не невозможно)
- Эмоциональной целостности (механики служат эмоции, не наоборот)
- Технических проблемах реализации
- Уважении к оригинальным стихам
- Согласованности с уже существующими системами
- Влиянии на производительность
- UX-проблемах (механика не должна фрустрировать)

Формат ответа:
1. **Общая оценка**: краткое резюме (1-2 предложения)
2. **Сильные стороны**: что хорошо и почему
3. **Проблемы**: конкретные проблемы с номерами
4. **Предложения**: конкретные исправления для каждой проблемы
5. **Вердикт**: ✅ Принять / ⚠️ Принять с доработками / ❌ Переделать

Будь честен, но конструктивен. Критикуй идеи, а не авторов.
Ответ на русском языке.`;

export async function runReviewer(context: AgentContext): Promise<AgentResult> {
  const { ai, prompt, previousResults } = context;

  // Рецензент работает с полным контекстом всех предыдущих агентов
  const fullContext = previousResults
    ?.map(r => `=== ${r.agent.toUpperCase()} ===\n${r.content}`)
    .join('\n\n') || '';

  const userMessage = [
    `[Исходный запрос]: ${prompt}`,
    fullContext && `[Предложения команды]:\n${fullContext}`,
    `\nПроведи критический разбор. Найди слабые места, логические дыры, проблемы баланса и эмоциональной целостности.`,
  ].filter(Boolean).join('\n\n');

  const startTime = Date.now();

  const response = await ai.chat.completions.create({
    messages: [
      { role: 'system', content: SYSTEM_PROMPT },
      { role: 'user', content: userMessage },
    ],
    temperature: 0.4, // Умеренная температура — нужна логика, но и гибкость
  });

  const content = response.choices?.[0]?.message?.content || 'Нет ответа';

  return {
    agent: 'reviewer',
    content,
    timestamp: Date.now(),
    duration: Date.now() - startTime,
  };
}
